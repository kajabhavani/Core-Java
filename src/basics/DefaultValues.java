package basics;

public class DefaultValues {

	static String course="java";
	static int id;
	static long mobile;
	static float marks;
	static double aggr;
	static String name;
	static boolean x;
	static char gender;
	
	
	public static void main(String[] args) {
		 
    System.out.println(course);
    System.out.println("default value assigned to int data type variable is: "+id);
    System.out.println("default value assigned to long data type variable is: "+mobile);
    System.out.println("default value assigned to float data type variable is: "+marks);
    System.out.println("default value assigned to double data type variable is: "+aggr);
    System.out.println("default value assigned to String data type variable is: "+name);
    System.out.println("default value assigned to boolean data type variable is: "+x);
    System.out.println("default value assigned to char data type variable is: "+gender);
    
    
   
    
	}

}
